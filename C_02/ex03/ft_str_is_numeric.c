int	ft_str_is_numeric(char *str)
{
	if (*str != '\0')
	{
		while (*str != '\0')
		{
			if (!(*str >= '0' && *str <= '9'))
			{
				return (0);
			}
			++str;
		}
		return (1);
	}
	else
	{
		return (1);
	}
	return (0);
}
