char	*ft_strupcase(char *str)
{
	char	*orgstr;

	orgstr = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str -= 32;
		}
		++str;
	}
	return (orgstr);
}
