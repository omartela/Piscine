char	*ft_strlowcase(char *str)
{
	char	*orgstr;

	orgstr = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str += 32;
		}
		++str;
	}
	return (orgstr);
}
